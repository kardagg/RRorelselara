# Function to rotate a mesh3d object about a given point using Euler angles
# and then translate it
kdEnvB3D$rotateAndTranslateMesh <- function(mesh, rotation_point, alpha, beta, gamma, displacement) {
  # Translate mesh to rotation point
  translated_mesh <- translate3d(mesh, -rotation_point[1], -rotation_point[2], -rotation_point[3])
  
  # Rotate mesh around the x-axis by alpha
  rotated_mesh <- rotate3d(translated_mesh, angle = alpha, x = -1, y = 0, z = 0)
  
  # Rotate mesh around the y-axis by beta
  rotated_mesh <- rotate3d(rotated_mesh, angle = beta, x = 0, y = -1, z = 0)
  
  # Rotate mesh around the z-axis by gamma
  rotated_mesh <- rotate3d(rotated_mesh, angle = gamma, x = 0, y = 0, z = -1)
  
  # Translate mesh back from rotation point and add additional displacement
  final_mesh <- translate3d(rotated_mesh, 
                            rotation_point[1] + displacement[1], 
                            rotation_point[2] + displacement[2], 
                            rotation_point[3] + displacement[3])
  
  return(final_mesh)
}

kdEnvB3D$rotateAndTranslateVector <- function(vector, rotation_point, alpha, beta, gamma, displacement) {

  
  # Define rotation matrices for each axis
  Rx <- matrix(c(1, 0, 0,
                 0, cos(alpha), -sin(alpha),
                 0, sin(alpha), cos(alpha)), nrow = 3, byrow = TRUE)
  
  Ry <- matrix(c(cos(beta), 0, sin(beta),
                 0, 1, 0,
                 -sin(beta), 0, cos(beta)), nrow = 3, byrow = TRUE)
  
  Rz <- matrix(c(cos(gamma), -sin(gamma), 0,
                 sin(gamma), cos(gamma), 0,
                 0, 0, 1), nrow = 3, byrow = TRUE)
  
  # Translate vector to the origin based on rotation point
  translated_vector <- vector - rotation_point
  
  # Apply rotations around x, y, and z axis
  rotated_vector <- Rx %*% translated_vector
  rotated_vector <- Ry %*% rotated_vector
  rotated_vector <- Rz %*% rotated_vector
  
  # Translate vector back to its original position and apply displacement
  final_vector <- rotated_vector + rotation_point + displacement
  
  return(final_vector)
}




# Function to add coordinate axes arrows with labels
kdEnvB3D$addAxesArrows <- function(origin = c(0, 0, 0), alpha=0, beta=0, gamma=0, length = 100, color = "red") {
  # Define rotation matrices for each axis
  Rx <- matrix(c(1, 0, 0,
                 0, cos(alpha), -sin(alpha),
                 0, sin(alpha), cos(alpha)), nrow = 3, byrow = TRUE)
  
  Ry <- matrix(c(cos(beta), 0, sin(beta),
                 0, 1, 0,
                 -sin(beta), 0, cos(beta)), nrow = 3, byrow = TRUE)
  
  Rz <- matrix(c(cos(gamma), -sin(gamma), 0,
                 sin(gamma), cos(gamma), 0,
                 0, 0, 1), nrow = 3, byrow = TRUE)
  
  R_combined <- Rz %*% Ry %*% Rx
  
  # Define endpoints of the arrows relative to the origin
  ends <- list(origin + c(R_combined %*% c(length, 0, 0)), 
                origin + c(R_combined %*% c(0, length, 0)), 
                origin + c(R_combined %*% c(0, 0, length)))


  # Draw arrows
  for (i in 1:length(ends)) {
    arrow3d(origin,ends[[i]],
            type = "rotation",
             col = color)
  }
  
 add <- 15
  # Add labels slightly past the tip of each arrow
  text3d(x = ends[[1]][1]+add , y = ends[[1]][2] , z = ends[[1]][3] ,
         texts = "x", 
         color=color,
         adj = c(0,0))
  text3d(x = ends[[2]][1] , y = ends[[2]][2]+2*add , z = ends[[2]][3] ,
         texts = "y", 
         color=color,
         adj = c(0,0))
  text3d(x = ends[[3]][1] , y = ends[[3]][2] , z = ends[[3]][3]+add ,
         texts = "z", 
         color=color,
         adj = c(0,0))

}


kdEnvB3D$rotateMatrix <- function(matrix, alpha, beta, gamma) {
  
  
  # Define rotation matrices for each axis
  Rx <- matrix(c(1, 0, 0,
                 0, cos(alpha), -sin(alpha),
                 0, sin(alpha), cos(alpha)), nrow = 3, byrow = TRUE)
  
  Ry <- matrix(c(cos(beta), 0, sin(beta),
                 0, 1, 0,
                 -sin(beta), 0, cos(beta)), nrow = 3, byrow = TRUE)
  
  Rz <- matrix(c(cos(gamma), -sin(gamma), 0,
                 sin(gamma), cos(gamma), 0,
                 0, 0, 1), nrow = 3, byrow = TRUE)
  
  R_combined <- Rz %*% Ry %*% Rx
  
  # Rotate the moment of inertia matrix
  matrix_rotated <- R_combined %*% matrix %*% t(R_combined)
  
  
  return(matrix_rotated)
}



