# Function: Plot 3D body  ------------------------------
kdDrawBody3D <- function(alphaDeg, betaDeg, gammaDeg, 
                        mass = 90,
                        length = 180,
                        inShiny = FALSE,
                        showGlobalAxes = FALSE,
                        showLocalAxes = FALSE,
                        selectedSegment = 1,
                        alphaChannel = 1.0,
                        showCenterOfGravity= TRUE, 
                        showForce = FALSE,
                        showJ=FALSE){
  
  # Variables -----------------------
  # Read from environment 
  bodySegmentsCM <- kdEnvB3D$bodySegmentsStartCM
  jointPositionsStart <- kdEnvB3D$jointPositionsStart
  jointTree <- kdEnvB3D$jointTree
  bodySegments <- kdEnvB3D$bodySegments
  
  # New variables
  jointPositionsNew <- jointPositionsStart
  bodySegmentsCMNew <- bodySegmentsCM
  
  alpha <- alphaDeg*pi/180
  beta <- betaDeg*pi/180
  gamma <- gammaDeg*pi/180
  centerOfMass <-  c(0,0,0)
  color <- "#f0e8e0";
 #color <- adjustcolor("#f0e8e0", alpha.f = alphaChannel )
  
  # Interior recursive functions
  
  #Transverse the segment tree and update center of mass
  recursiveCMUpdate <- function(indices){
    
    segmentIndex <- indices[[1]]
    
    # <<- to set the value in the parent function
    
    # add to center of mass
    bodySegmentsCMNew[[segmentIndex]] <<- kdEnvB3D$rotateAndTranslateVector(
      bodySegmentsCM[[segmentIndex]],
      rotation_point = jointPositionsStart[[segmentIndex]],
      alpha=alpha[[segmentIndex]],
      beta=beta[[segmentIndex]],
      gamma=gamma[[segmentIndex]],
      displacement = jointPositionsNew[[segmentIndex]]-jointPositionsStart[[segmentIndex]]
    )
    centerOfMass <<- c(centerOfMass + kdEnvB3D$bodySegmentsMassRatio[segmentIndex]*bodySegmentsCMNew[[segmentIndex]])
    
    #recursive calls
    if(length(indices)>1){
      
      for(i in 2:length(indices)){
        jointPositionsNew[[indices[[i]]]] <<- c(kdEnvB3D$rotateAndTranslateVector(
          jointPositionsStart[[indices[[i]]]],
          rotation_point = jointPositionsStart[[segmentIndex]],
          alpha=alpha[[segmentIndex]],
          beta=beta[[segmentIndex]],
          gamma=gamma[[segmentIndex]],
          displacement = jointPositionsNew[[segmentIndex]]-jointPositionsStart[[segmentIndex]]))
        
        #recursive
        recursiveCMUpdate(jointTree[[indices[[i]]]])
      }
    }
  }
  
  #Transverse the segment tree and update geometry
  recursiveSegmentGeometryUpdate <- function(indices){
    
    segmentIndex <- indices[[1]]
    
    # <<- to set the value in the parent function
    # update segment coordinates
    bodySegments[[segmentIndex]] <<- kdEnvB3D$rotateAndTranslateMesh(
      bodySegments[[segmentIndex]],
      rotation_point = jointPositionsStart[[segmentIndex]],
      alpha=alpha[[segmentIndex]],
      beta=beta[[segmentIndex]],
      gamma=gamma[[segmentIndex]],
      displacement = 
        jointPositionsNew[[segmentIndex]]-jointPositionsStart[[segmentIndex]]-centerOfMass
    )
    
  
    #recursive calls
    if(length(indices)>1){
      
      for(i in 2:length(indices)){
        jointPositionsNew[[indices[[i]]]] <<- c(kdEnvB3D$rotateAndTranslateVector(
          jointPositionsStart[[indices[[i]]]],
          rotation_point = jointPositionsStart[[segmentIndex]],
          alpha=alpha[[segmentIndex]],
          beta=beta[[segmentIndex]],
          gamma=gamma[[segmentIndex]],
          displacement = jointPositionsNew[[segmentIndex]]-jointPositionsStart[[segmentIndex]]))
        
        #recursive
        recursiveSegmentGeometryUpdate(jointTree[[indices[[i]]]])
      }
    }
  }
  
  
  # Main program 
  recursiveCMUpdate(jointTree[[1]])
  recursiveSegmentGeometryUpdate(jointTree[[1]])
  
  
  # Graphics
  open3d(useNULL = inShiny) # Open a new rgl for rglWidget i shiny if true
  
  # Adjusting material properties for a less shiny appearance
  material3d(shininess = 20, specular = "black")
  
  
  #Light
  #light3d(theta = 45, phi = 30, diffuse = "white", specular = "white", ambient = "#FFFFFF", viewpoint.rel = FALSE)
  
  for(i in 1:14){
    shade3d(bodySegments[[i]], color = color, alpha=alphaChannel)
  }
  
  # Add global axes 
  if(showGlobalAxes){
    kdEnvB3D$addAxesArrows (origin = c(-600, -300, -300), length = 300, color="blue") 
  }
  
  #Add local axes for selected segment
  if(showLocalAxes){
    kdEnvB3D$addAxesArrows (origin = c(bodySegmentsCMNew[[selectedSegment]]-centerOfMass), 
                            alpha=alpha[selectedSegment],
                            beta=beta[selectedSegment],
                            gamma=gamma[selectedSegment],
                            length = 300, color="#880088") 
  }

  if(showCenterOfGravity){
    spheres3d(x=0, y=0,z=0, color="red", radius=20)
  }
  
  if(showForce){
    arrow3d(c(0,0,0),c(0,0,-mass*10), color="red", type = "rotation", barblen = .07)
  }
  
  if(showJ){
    totalJMatrix <- matrix(
      c(0, 0, 0, 0, 0, 0, 0, 0, 0), 
      ncol = 3, byrow = TRUE)
    

    #Rotera matriser
    for(i in 1:14){
     segmentJMatrix <-  (mass/90)*(length*length/32400)*
       kdEnvB3D$rotateMatrix(
         kdEnvB3D$momentOfInertiaMatrixes[[i]],alpha[i],beta[i],gamma[i])
  
      
     #displacement segment cm to global cm
     displacement <- c(centerOfMass-bodySegmentsCMNew[[i]])/1000
    

     #Beräkna kring tyngdpunkt
     totalJMatrix <- totalJMatrix+segmentJMatrix + 
       mass*kdEnvB3D$bodySegmentsMassRatio[i]*((norm(displacement, type="2")^2)*diag(3)-
                                                 outer(displacement, displacement))
     
    }
    
    #Huvudtröghetsaxlar
    eigenAnalys <- eigen(totalJMatrix)
    principalAxes <- eigenAnalys$vectors
    
    #Draw
    jColor <- "#007700"
    scale <- 900
    scaleT <- 930
    
    principalValues <- eigenAnalys$values
    for(i in 1:3){
      segments3d(x=c(-1,1)*principalAxes[1,i]*scale,
                 y=c(-1,1)*principalAxes[2,i]*scale,
                 z=c(-1,1)*principalAxes[3,i]*scale,
                 color=jColor, lwd=4)
      
      text3d(x = principalAxes[1,i]*scaleT , 
             y = principalAxes[2,i]*scaleT , 
             z = principalAxes[3,i]*scaleT,
             texts = paste(toString(round(principalValues[i], digits = 1)),"kgm\ub2"), 
             color=jColor,
             adj = c(0,1))
      

    }
  }

}


